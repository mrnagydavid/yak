// Chunk words into batches for the translation-curator subagent (layer 40), which verifies/improves
// each entry's main translation and rebuilds its complete meaning list.
//
// Fix for design bug #2: the curator now sees a STABLE, FROZEN input — the word as resolved from
// base + the layers below 40 (cleaner/pos/subdef), NOT the freshly-built shipped seed. Each item
// carries its `inputHash`; the agent echoes it back into its answer so staleness is mechanical.
//
// Selection (design §4.6): by default only STALE (input changed since last pass) + NEW (never curated)
// words are batched — no full sweeps. Set SEED_BATCH_ALL=1 to force a full pass.
// Output: data/scratch/sv/tr-batches/<n>.json
// Run: node scripts/seed/batch-translations.mjs   (after seed:apply + seed:stale)
import { existsSync } from 'node:fs'
import { mkdir, readFile, readdir, rm, writeFile } from 'node:fs/promises'
import { assemble, layerDir, loadManifest, SCRATCH_DIR, shortHash, wordFrozenInput } from '../lib/layers.mjs'

const BATCH_DIR = `${SCRATCH_DIR}/tr-batches`
const WIK = `${SCRATCH_DIR}/wik.json` // context only (not part of inputHash); regenerated by seed:fetch
const BATCH_SIZE = 100 // entries per batch (each carries its full Wiktionary sense list for context)
const DATE = new Date().toISOString().slice(0, 10) // batch/answer files are dated so compile's newest-per-word wins
const CEFR_RANK = { A1: 1, A2: 2, B1: 3, B2: 4, C1: 5, C2: 6 }

// The Wiktionary senses this entry could draw from — full meaning picture for the curator (may be
// empty for phrases/unmatched lemmas, or if wik.json hasn't been fetched into scratch).
function wikSenses(wik, lemma, pos, gender) {
  const all = wik[lemma] ?? []
  const exact = all.filter((w) => w.pos === pos)
  const genderMatch = pos === 'noun' && gender ? exact.filter((w) => w.gender === gender) : []
  const preferred = genderMatch.length ? genderMatch : exact
  const wiks = preferred.length ? preferred : all
  return [...new Set(wiks.flatMap((w) => w.glosses ?? []))]
}

async function main() {
  const manifest = await loadManifest()
  const layer = manifest.find((l) => l.kind === 'translation')
  const { finalEntries } = await assemble(manifest, { upToExclusive: layer.id })
  const wik = existsSync(WIK) ? JSON.parse(await readFile(WIK, 'utf-8')) : {}
  if (!existsSync(WIK)) console.warn(`warning: ${WIK} not found — batching without Wiktionary context (run pnpm seed:fetch)`)

  const dir = layerDir(layer)
  const curated = new Set(existsSync(`${dir}/decisions.json`) ? JSON.parse(await readFile(`${dir}/decisions.json`, 'utf-8')).map((d) => d.kellyId) : [])
  const staleKeys = new Set(existsSync(`${dir}/stale.json`) ? JSON.parse(await readFile(`${dir}/stale.json`, 'utf-8')).map((s) => s.key) : [])
  const all = process.env.SEED_BATCH_ALL === '1'

  const items = finalEntries
    .filter((e) => all || staleKeys.has(e.kellyId) || curated.has(e.kellyId) === false)
    .map((e) => ({
      kellyId: e.kellyId,
      lemma: e.lemma,
      pos: e.pos,
      ...(e.gender ? { gender: e.gender } : {}),
      cefr: e.cefr,
      currentTranslation: e.translation,
      ...(e.subDefinitions?.length ? { currentSubDefinitions: e.subDefinitions } : {}),
      ...(e.examples?.length ? { examples: e.examples } : {}),
      wiktionarySenses: wikSenses(wik, e.lemma, e.pos, e.gender),
      inputHash: shortHash(wordFrozenInput(e)), // echo back unchanged in the answer
    }))
    .sort((a, b) => (CEFR_RANK[a.cefr] ?? 9) - (CEFR_RANK[b.cefr] ?? 9) || a.lemma.localeCompare(b.lemma, 'sv'))

  if (items.length === 0) {
    console.log('nothing to batch (no stale or new words — run with SEED_BATCH_ALL=1 for a full pass)')
    return
  }
  if (existsSync(BATCH_DIR)) for (const f of await readdir(BATCH_DIR)) await rm(`${BATCH_DIR}/${f}`)
  await mkdir(BATCH_DIR, { recursive: true })
  let n = 0
  for (let i = 0; i < items.length; i += BATCH_SIZE) {
    await writeFile(`${BATCH_DIR}/${DATE}-${String(n).padStart(3, '0')}.json`, JSON.stringify(items.slice(i, i + BATCH_SIZE), null, 2))
    n++
  }
  console.log(`wrote ${n} translation batches (${items.length} entries${all ? ', full pass' : ', stale+new'}, size ${BATCH_SIZE}) → ${BATCH_DIR}`)
}

main().catch((e) => {
  console.error(e)
  process.exit(1)
})
