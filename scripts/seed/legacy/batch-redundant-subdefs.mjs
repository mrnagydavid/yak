// TARGETED cleanup: batch only the words whose `subDefinitions` list carries a piece that REUSES an
// English word already shown as the main translation or a promoted meaning — the residue the
// "other-meanings-only" sweep left behind (parenthetical nuances like `filosofi → philosophy
// (personal outlook)` and paraphrases like `svära → swear, take an oath`). See SEED-PIPELINE-DESIGN.md
// §4.8 and the "other English WORDS, not nuances" refinement.
//
// This is a DETECTOR-for-batching, not the guard: the head-overlap test deliberately OVER-selects —
// it also catches genuinely-distinct senses that happen to share the English word (`article
// (grammar)`, `bank (of a river)`, `flame (intense love or passion)`). Those are KEPT; the LLM makes
// the keep/drop/strip judgment per SEED-PIPELINE-DESIGN.md. The committed standing guard
// (audit-subdefs.mjs) stays the bare-restatement check — this semantic line is not mechanically
// enforceable, by design (Option B).
//
// Only words whose list is OWNED BY LAYER 40 are batched. Split (layer 45) and 90-manual words are
// excluded (their lists are owned higher up); handle those by hand if any are flagged.
//
// The agent (subdef-deduplicator) rewrites `senses` only. It must NOT change the main translation —
// that is re-injected verbatim by stamp-redundant-subdefs.mjs, so the main-translation invariant
// holds regardless of the agent's output. Answers go to scratch (NOT runs/); the stamp step writes
// the finalized run files.
//
// Output: data/scratch/sv/subdef-dedup-batches/<n>.json   (agent answers → subdef-dedup-answers/)
// Run: node scripts/seed/batch-redundant-subdefs.mjs   (after seed:build so seed-sv.json is current)
import { existsSync } from 'node:fs'
import { mkdir, readFile, readdir, rm, writeFile } from 'node:fs/promises'
import { assemble, CEFR_RANK, flatten, layerDir, loadManifest, SCRATCH_DIR, SEED_DIR, shortHash, wordFrozenInput } from '../lib/layers.mjs'

const BATCH_DIR = `${SCRATCH_DIR}/subdef-dedup-batches`
const WIK = `${SCRATCH_DIR}/wik.json` // context only (not part of inputHash); regenerated by seed:fetch
const BATCH_SIZE = 100

// A translation's comma/semicolon pieces, each normalized (lowercased, article/"to" dropped).
const pieces = (s) => (s ?? '').split(/[;,]/).map(flatten).filter(Boolean)
// A subDefinition piece's head word — the parenthetical disambiguator stripped before normalizing.
const head = (p) => flatten(p.split('(')[0])

function wikSenses(wik, lemma, pos, gender) {
  const all = wik[lemma] ?? []
  const exact = all.filter((w) => w.pos === pos)
  const genderMatch = pos === 'noun' && gender ? exact.filter((w) => w.gender === gender) : []
  const preferred = genderMatch.length ? genderMatch : exact
  const wiks = preferred.length ? preferred : all
  return [...new Set(wiks.flatMap((w) => w.glosses ?? []))]
}

async function readJson(p, fallback) {
  return existsSync(p) ? JSON.parse(await readFile(p, 'utf-8')) : fallback
}

async function main() {
  const manifest = await loadManifest()
  const translationLayer = manifest.find((l) => l.kind === 'translation')

  // Frozen input (base + layers below 40) — the source of each word's inputHash and metadata.
  const { finalEntries: below40 } = await assemble(manifest, { upToExclusive: translationLayer.id })
  const belowByKey = new Map(below40.map((e) => [e.kellyId, e]))

  // Shipped seed — the current main translation + list the learner actually sees (what we re-curate).
  const seed = await readJson(`${SEED_DIR}/seed-sv.json`, { entries: [] })
  const shipped = seed.entries ?? seed

  // Words whose list is NOT owned by layer 40 → excluded from the LLM sweep.
  const split = await readJson(`${layerDir(manifest.find((l) => l.kind === 'split'))}/decisions.json`, [])
  const excluded = new Set(split.map((d) => d.kellyId))
  const manualDir = layerDir(manifest.find((l) => l.id === 90))
  for (const f of (await readdir(manualDir)).filter((x) => x.endsWith('.json') && x !== 'stale.json')) {
    for (const r of await readJson(`${manualDir}/${f}`, [])) {
      if (Array.isArray(r.altMeanings) || r.proposedSubDefinitions) excluded.add(r.kellyId)
    }
  }

  const wik = await readJson(WIK, {})
  if (!existsSync(WIK)) console.warn(`warning: ${WIK} not found — batching without Wiktionary context (run pnpm seed:fetch)`)

  // Candidate = has a list, layer-40-owned, and at least one subDef piece whose head reuses an
  // English word already shown as the main translation or a promoted meaning.
  const isCandidate = (e) => {
    const taken = new Set(pieces(e.translation))
    for (const m of e.altMeanings ?? []) for (const p of pieces(m.translation)) taken.add(p)
    return e.subDefinitions.some((sd) => sd.split(/[;,]/).some((piece) => taken.has(head(piece))))
  }

  const items = shipped
    .filter((e) => e.subDefinitions?.length && !excluded.has(e.seedKey) && belowByKey.has(e.seedKey) && isCandidate(e))
    .map((e) => {
      const below = belowByKey.get(e.seedKey)
      return {
        kellyId: e.seedKey,
        lemma: e.lemma,
        pos: e.pos,
        ...(e.gender ? { gender: e.gender } : {}),
        cefr: e.cefr,
        mainTranslation: e.translation, // FIXED — do not change; re-injected by the stamp step
        promotedMeanings: (e.altMeanings ?? []).map((m) => m.translation), // already their own cards
        currentSubDefinitions: e.subDefinitions, // the list to clean
        wiktionarySenses: wikSenses(wik, e.lemma, e.pos, e.gender),
        inputHash: shortHash(wordFrozenInput(below)),
      }
    })
    .sort((a, b) => (CEFR_RANK[a.cefr] ?? 9) - (CEFR_RANK[b.cefr] ?? 9) || a.lemma.localeCompare(b.lemma, 'sv'))

  if (existsSync(BATCH_DIR)) for (const f of await readdir(BATCH_DIR)) await rm(`${BATCH_DIR}/${f}`)
  await mkdir(BATCH_DIR, { recursive: true })
  let n = 0
  for (let i = 0; i < items.length; i += BATCH_SIZE) {
    await writeFile(`${BATCH_DIR}/${String(n).padStart(3, '0')}.json`, JSON.stringify(items.slice(i, i + BATCH_SIZE), null, 2))
    n++
  }
  console.log(`wrote ${n} redundant-subdef batches (${items.length} owner-40 candidate words, size ${BATCH_SIZE}) → ${BATCH_DIR}`)
}

main().catch((e) => {
  console.error(e)
  process.exit(1)
})
